
public class TestaCaracteres {

	public static void main(String[] args) {
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String sValue = "Teste";
		System.out.println(sValue);
		
		sValue = sValue + 2020;
		System.out.println(sValue);
		
		String parc01 = "10";
		String parc02 = "20";
		System.out.println(parc01+parc02);
	}
}
